    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaSelecionada = tblComputador.getSelectedRow();
        int id = Integer.parseInt(tblComputador.getValueAt(linhaSelecionada,0).toString());
        String hd = tblComputador.getValueAt(linhaSelecionada,2).toString();
        String processador = tblComputador.getValueAt(linhaSelecionada,3).toString();
        
        Computador obj = new Computador(id,hd, processador);
        
        TelaCadastro telaCadastro = new TelaCadastro(obj);
        
        JDesktopPane desktopPane=getDesktopPane();
        desktopPane.add(telaCadastro);
        telaCadastro.show();
        
        
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblComputador.getSelectedRow();
        int id = Integer.parseInt(tblComputador.getValueAt(linhaSelecionada,0).toString());
        boolean retorno = ComputadorDAO.excluir(id);
        if(retorno){
            JOptionPane.showMessageDialog(this, "Nota excluída com sucesso!");
        }else{
            JOptionPane.showMessageDialog(this, "Falha ao excluir a nota!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


