    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if (txtID.getText().length() == 0 && txtProcessador.getText().length() == 0) {

            ArrayList<Computador> lista = ComputadorDAO.listar();
            DefaultTableModel modelo = (DefaultTableModel) tblComputador.getModel();
            modelo.setRowCount(0);

            for (Computador item : lista) {
                modelo.addRow(new String[]{String.valueOf(item.getId()),
                                           String.valueOf(item.getMarca()),
                                           String.valueOf(item.getHd()),
                                           String.valueOf(item.getProcessador())});
            }
        }else if(txtID.getText().length() > 0 && txtProcessador.getText().length() == 0){
            
            
            
            int id = Integer.parseInt(txtID.getText());
            Computador obj = ComputadorDAO.buscarPorID(id);
            
            DefaultTableModel modelo = (DefaultTableModel) tblComputador.getModel();
            
            modelo.setRowCount(0);
            
            modelo.addRow(new String[]{String.valueOf(obj.getId()),
                                        String.valueOf(obj.getMarca()),
                                        String.valueOf(obj.getHd()),
                                        String.valueOf(obj.getProcessador())});
        }else if (txtID.getText().length() == 0 && txtProcessador.getText().length() > 0){
            String processador = txtProcessador.getText();
            Computador obj = ComputadorDAO.buscarPorProcessador(processador);
            
            DefaultTableModel modelo = (DefaultTableModel) tblComputador.getModel();

            modelo.setRowCount(0);
            
            modelo.addRow(new String[]{String.valueOf(obj.getId()),
                                        String.valueOf(obj.getMarca()),
                                        String.valueOf(obj.getHd()),
                                        String.valueOf(obj.getProcessador())}); 
        }else{
            JOptionPane.showMessageDialog(this, "Preencha somente um dos campos");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

